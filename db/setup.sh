#!/bin/bash

# HR Candidate Evaluation System - Database Setup Script
# This script automates the complete database setup process

set -e  # Exit on any error

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Configuration
DB_NAME="hr_candidate_eval"
DB_USER="hr_eval_app"
POSTGRES_USER="postgres"
POSTGRES_HOST="localhost"
POSTGRES_PORT="5432"

# Function to print colored output
print_status() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

print_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

print_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

print_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# Function to check if PostgreSQL is running
check_postgres() {
    print_status "Checking PostgreSQL connection..."
    
    if ! pg_isready -h "$POSTGRES_HOST" -p "$POSTGRES_PORT" -U "$POSTGRES_USER" >/dev/null 2>&1; then
        print_error "PostgreSQL is not running or not accessible"
        print_error "Please ensure PostgreSQL is installed and running on $POSTGRES_HOST:$POSTGRES_PORT"
        exit 1
    fi
    
    print_success "PostgreSQL is running and accessible"
}

# Function to check if database exists
check_database_exists() {
    local db_exists=$(psql -h "$POSTGRES_HOST" -p "$POSTGRES_PORT" -U "$POSTGRES_USER" -tAc "SELECT 1 FROM pg_database WHERE datname='$DB_NAME'" 2>/dev/null || echo "")
    
    if [ "$db_exists" = "1" ]; then
        return 0  # Database exists
    else
        return 1  # Database does not exist
    fi
}

# Function to check if user exists
check_user_exists() {
    local user_exists=$(psql -h "$POSTGRES_HOST" -p "$POSTGRES_PORT" -U "$POSTGRES_USER" -tAc "SELECT 1 FROM pg_roles WHERE rolname='$DB_USER'" 2>/dev/null || echo "")
    
    if [ "$user_exists" = "1" ]; then
        return 0  # User exists
    else
        return 1  # User does not exist
    fi
}

# Function to prompt for password
prompt_password() {
    local prompt="$1"
    local password
    
    echo -n "$prompt"
    read -s password
    echo
    echo "$password"
}

# Function to run SQL script
run_sql_script() {
    local script_path="$1"
    local database="$2"
    local description="$3"
    
    print_status "$description"
    
    if [ ! -f "$script_path" ]; then
        print_error "Script not found: $script_path"
        exit 1
    fi
    
    if [ -n "$database" ]; then
        if ! psql -h "$POSTGRES_HOST" -p "$POSTGRES_PORT" -U "$POSTGRES_USER" -d "$database" -f "$script_path" >/dev/null 2>&1; then
            print_error "Failed to execute $script_path"
            exit 1
        fi
    else
        if ! psql -h "$POSTGRES_HOST" -p "$POSTGRES_PORT" -U "$POSTGRES_USER" -f "$script_path" >/dev/null 2>&1; then
            print_error "Failed to execute $script_path"
            exit 1
        fi
    fi
    
    print_success "$description completed"
}

# Function to test database connection with app user
test_app_connection() {
    print_status "Testing application user connection..."
    
    # Prompt for app user password
    local app_password=$(prompt_password "Enter password for $DB_USER: ")
    
    if PGPASSWORD="$app_password" psql -h "$POSTGRES_HOST" -p "$POSTGRES_PORT" -U "$DB_USER" -d "$DB_NAME" -c "SELECT 1;" >/dev/null 2>&1; then
        print_success "Application user connection successful"
        
        # Save connection details to .env file
        create_env_file "$app_password"
    else
        print_error "Failed to connect with application user"
        print_warning "Please check the password and try again"
        return 1
    fi
}

# Function to create .env file
create_env_file() {
    local app_password="$1"
    local env_file="../.env"
    
    print_status "Creating environment configuration..."
    
    # Create .env file with database configuration
    cat > "$env_file" << EOF
# HR Candidate Evaluation System - Database Configuration
# Generated by setup script on $(date)

# Groq API Configuration
VITE_GROQ_API_KEY=your_groq_api_key_here

# API Configuration
VITE_API_URL=http://localhost:3001/api

# PostgreSQL Configuration
POSTGRES_HOST=$POSTGRES_HOST
POSTGRES_PORT=$POSTGRES_PORT
POSTGRES_DB=$DB_NAME
POSTGRES_USER=$DB_USER
POSTGRES_PASSWORD=$app_password

# Server Configuration
PORT=3001
HOST=0.0.0.0

# Node.js Environment
NODE_ENV=development

# Optional: SSL Configuration (set to true for production)
POSTGRES_SSL=false

# Database Schema (using dbo schema instead of public)
POSTGRES_SCHEMA=dbo

# Get your Groq API key from: https://console.groq.com/keys
EOF
    
    print_success "Environment file created: $env_file"
    print_warning "Remember to set your VITE_GROQ_API_KEY in the .env file"
}

# Function to display usage
show_usage() {
    echo "Usage: $0 [OPTIONS]"
    echo ""
    echo "Options:"
    echo "  -h, --help              Show this help message"
    echo "  -f, --force             Force recreation of database (drops existing)"
    echo "  -s, --skip-sample       Skip sample data insertion"
    echo "  -t, --test-only         Only test connection, don't create anything"
    echo "  --host HOST             PostgreSQL host (default: localhost)"
    echo "  --port PORT             PostgreSQL port (default: 5432)"
    echo "  --postgres-user USER    PostgreSQL superuser (default: postgres)"
    echo ""
    echo "Examples:"
    echo "  $0                      # Standard setup"
    echo "  $0 --force              # Force recreation"
    echo "  $0 --skip-sample        # Setup without sample data"
    echo "  $0 --host myserver      # Use different host"
}

# Function to verify script files
verify_scripts() {
    local scripts=(
        "01_create_database.sql"
        "02_create_schema.sql"
        "03_performance_indexes.sql"
        "04_security_setup.sql"
        "05_sample_data.sql"
    )
    
    print_status "Verifying script files..."
    
    for script in "${scripts[@]}"; do
        if [ ! -f "$script" ]; then
            print_error "Required script not found: $script"
            print_error "Please ensure all database scripts are in the current directory"
            exit 1
        fi
    done
    
    print_success "All required scripts found"
}

# Main setup function
main_setup() {
    local force_recreate=false
    local skip_sample=false
    local test_only=false
    
    # Parse command line arguments
    while [[ $# -gt 0 ]]; do
        case $1 in
            -h|--help)
                show_usage
                exit 0
                ;;
            -f|--force)
                force_recreate=true
                shift
                ;;
            -s|--skip-sample)
                skip_sample=true
                shift
                ;;
            -t|--test-only)
                test_only=true
                shift
                ;;
            --host)
                POSTGRES_HOST="$2"
                shift 2
                ;;
            --port)
                POSTGRES_PORT="$2"
                shift 2
                ;;
            --postgres-user)
                POSTGRES_USER="$2"
                shift 2
                ;;
            *)
                print_error "Unknown option: $1"
                show_usage
                exit 1
                ;;
        esac
    done
    
    # Print setup information
    echo "=============================================="
    echo "HR Candidate Evaluation System - Database Setup"
    echo "=============================================="
    echo "Database: $DB_NAME"
    echo "App User: $DB_USER"
    echo "PostgreSQL Host: $POSTGRES_HOST:$POSTGRES_PORT"
    echo "PostgreSQL User: $POSTGRES_USER"
    echo "=============================================="
    echo ""
    
    # Check prerequisites
    check_postgres
    verify_scripts
    
    # Test only mode
    if [ "$test_only" = true ]; then
        print_status "Test mode - checking existing setup..."
        
        if check_database_exists; then
            print_success "Database '$DB_NAME' exists"
        else
            print_warning "Database '$DB_NAME' does not exist"
        fi
        
        if check_user_exists; then
            print_success "User '$DB_USER' exists"
            test_app_connection
        else
            print_warning "User '$DB_USER' does not exist"
        fi
        
        exit 0
    fi
    
    # Check if database already exists
    if check_database_exists; then
        if [ "$force_recreate" = true ]; then
            print_warning "Database exists - forcing recreation"
            print_status "Dropping existing database..."
            psql -h "$POSTGRES_HOST" -p "$POSTGRES_PORT" -U "$POSTGRES_USER" -c "DROP DATABASE IF EXISTS $DB_NAME;" >/dev/null 2>&1
            print_success "Existing database dropped"
        else
            print_warning "Database '$DB_NAME' already exists"
            print_warning "Use --force to recreate or --test-only to test existing setup"
            exit 1
        fi
    fi
    
    # Check if user already exists
    if check_user_exists; then
        if [ "$force_recreate" = true ]; then
            print_warning "User exists - forcing recreation"
            print_status "Dropping existing user..."
            psql -h "$POSTGRES_HOST" -p "$POSTGRES_PORT" -U "$POSTGRES_USER" -c "DROP USER IF EXISTS $DB_USER;" >/dev/null 2>&1
            print_success "Existing user dropped"
        else
            print_warning "User '$DB_USER' already exists"
            print_warning "Use --force to recreate"
        fi
    fi
    
    # Run setup scripts
    print_status "Starting database setup..."
    
    # Step 1: Create database and user
    run_sql_script "01_create_database.sql" "" "Creating database and user"
    
    # Step 2: Create schema
    run_sql_script "02_create_schema.sql" "$DB_NAME" "Creating database schema"
    
    # Step 3: Add performance optimizations
    run_sql_script "03_performance_indexes.sql" "$DB_NAME" "Adding performance optimizations"
    
    # Step 4: Set up security
    run_sql_script "04_security_setup.sql" "$DB_NAME" "Setting up security features"
    
    # Step 5: Load sample data (optional)
    if [ "$skip_sample" = false ]; then
        run_sql_script "05_sample_data.sql" "$DB_NAME" "Loading sample data"
    else
        print_status "Skipping sample data insertion"
    fi
    
    print_success "Database setup completed successfully!"
    
    # Test connection
    test_app_connection
    
    # Final instructions
    echo ""
    echo "=============================================="
    echo "Setup Complete!"
    echo "=============================================="
    echo "Database: $DB_NAME"
    echo "App User: $DB_USER"
    echo "Connection: $POSTGRES_HOST:$POSTGRES_PORT"
    echo ""
    echo "Next steps:"
    echo "1. Update your .env file with the Groq API key"
    echo "2. Start the backend server: cd server && npm run dev"
    echo "3. Start the frontend: npm run dev"
    echo ""
    
    if [ "$skip_sample" = false ]; then
        echo "Sample user accounts (password: password123):"
        echo "- admin@hreval.com (Administrator)"
        echo "- hr.manager@company.com (HR Manager)"
        echo "- tech.lead@company.com (Technical Lead)"
        echo "- recruiter@company.com (Recruiter)"
        echo "- candidate@example.com (Candidate)"
        echo ""
    fi
    
    echo "For troubleshooting, see DB/README.md"
    echo "=============================================="
}

# Check if running from correct directory
if [ ! -f "01_create_database.sql" ]; then
    print_error "Please run this script from the DB directory"
    print_error "cd DB && ./setup.sh"
    exit 1
fi

# Run main setup
main_setup "$@"